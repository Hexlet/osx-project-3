#pragma mark - Fetching

- (void)performFetch
{
    for (NSFetchedResultsController *glagneItem in self.sections) {
        [glagneItem performFetch:NULL];
    }
    [self.tableView reloadData];
}

- (void)setSections:(NSArray *)sections {
    if (_sections != sections) {
        _sections = sections;
        for (NSFetchedResultsController *glagneItem in sections) {
            [glagneItem setDelegate:self];
            [glagneItem performFetch:NULL];
        }
    }
    [self.tableView reloadData];
}

